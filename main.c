static t_list	*get_next_min(t_list **stack)
{
	t_list	*head;
	t_list	*min;
	int		has_min;

	min = NULL;
	has_min = 0;
	head = *stack;
	if (head)
	{
		while (head)
		{
			if ((head->index == -1) && (!has_min || head->value < min->value))
			{
				min = head;
				has_min = 1;
			}
			head = head->next;
		}
	}
	return (min);
}


void	index_stack(t_list **stack)
{
	t_list	*head;
	int		index;

	index = 0;
	head = get_next_min(stack);
	while (head)
	{
		head->index = index++;
		head = get_next_min(stack);
	}
}


//verilen listenin son düğümünü bulmak için fonk
t_list	*ft_lstlast(t_list *head)
{
	t_list	*tmp;

	tmp = head;  // head listenin başını işaret eden işaretçi tmp tutuyor 
	while (tmp->next)  //hep bir donrakine gideceğiz ve tmp null olmadığı sürece
	{
		tmp = tmp->next;  // burada bir sonraki null ise onu iaretçisini döndürüyoruz
		if (tmp->next == NULL)
			return (tmp);
	}
	return (tmp);
}



// Adds the specified node to a stack (list) making it the last node
void	ft_lstadd_back(t_list **stack, t_list *new)
{
	t_list	*n;   //listenin son düğümünü işaret edecek

	if (*stack)   //eğer liste boş değilse son düğümü bulur 
	{
		n = ft_lstlast(*stack);
		n->next = new;
		new->next = NULL;   // sadece önlem ve okunabilirlilikiçin tekrar null ladım . listenin sonunda olduğunu göstermek için
	}
	else               // liste boş ise yeni düğüm listenin ilk ve tek düğümü olur
	{
		*stack = new;
		(*stack)->next = NULL; // yeni düğümün nexti null olur son ve tek düğüm olduğu için 
	}
}


t_list	*ft_lstnew(int value)
{
	t_list	*new;

	new = (t_list *) malloc(sizeof(*new));
	if (!new)
		return (NULL);
	new->value = value;
	new->index = -1;   //düğüm henüz sııralanmadı oluşturuldu sadece
	new->next = NULL;  //düğüm şuanda listenin son düğümü
	return (new);
}

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	size_t	count;
	size_t	size;
	char	*str;

	if (!s)
		return (NULL);
	if ((unsigned int)ft_strlen(s) < start)
		return (ft_strdup(""));
	size = ft_strlen(s + start);
	if (size < len)
		len = size;
	str = (char *)malloc((len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	count = 0;
	while (count < len)
	{
		str[count] = s[start + count];
		count++;
	}
	str[count] = '\0';
	return (str);
}


static size_t	ft_kelen(const char *s, char c)
{
	size_t	ret;

	ret = 0;
	while (*s)
	{
		if (*s != c)
		{
			++ret;
			while (*s && *s != c)
				++s;
		}
		else
			++s;
	}
	return (ret);
}


static void	initStack(t_list **stack, int argc, char **argv)
{
	t_list	*new;   // yeni listenin düğümünün işaretçisi
	char	**args;  // argümanları tutacak
	int		i;

	i = 0;
	if (argc == 2)
		args = ft_split(argv[1], ' ');
	else
	{
		i = 1;
		args = argv;
	}
	while (args[i])
	{
		new = ft_lstnew(ft_atoi(args[i]));   //burada değerler için tek tek düğüm oluşturluyor ama indekslenmiyor rsadece oluşturuluyor 
		ft_lstadd_back(stack, new); // oluşturuluan düğümleri burada listeye ekler
		i++;
	}
	index_stack(stack);
	if (argc == 2)
		ft_free(args);
}

